package com.ozen.grup32java.ders4;

public class Ornekler {
    public void sayiyiRakamaCevir(int rakam) {
        if (rakam == 0) {
            System.out.println("Girdiğiniz Sayı sıfırdır");
        } else if (rakam == 1) {
            System.out.println("Girdiğiniz Sayı birdir" );
        } else if (rakam == 2) {
            System.out.println("Girdiğiniz Sayı ikidir" );
        } else if (rakam == 3) {
            System.out.println("Girdiğiniz Sayı üçtür" );
        } else if (rakam == 4) {
            System.out.println("Girdiğiniz Sayı dörttür" );
        } else if (rakam == 5) {
            System.out.println("Girdiğiniz Sayı beştir" );
        } else if (rakam == 6) {
            System.out.println("Girdiğiniz Sayı altıdır" );
        } else if (rakam == 7) {
            System.out.println("Girdiğiniz Sayı yedidir" );
        } else if (rakam == 8) {
            System.out.println("Girdiğiniz Sayı sekizdir");
        } else if (rakam == 9){
            System.out.println("Girdiğiniz Sayı dokuzdur");
        }else
            System.out.println("Yanlış Sayı Girdiniz");
    }
}